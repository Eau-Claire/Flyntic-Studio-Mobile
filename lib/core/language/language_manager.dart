import 'package:flutter/material.dart';
import 'package:shared_preferences/shared_preferences.dart';

enum Language { vi, en }

class LanguageManager extends ChangeNotifier {
  static final LanguageManager instance = LanguageManager._internal();
  LanguageManager._internal();

  Language _language = Language.vi;
  SharedPreferences? _prefs;

  Language get language => _language;
  bool get isVietnamese => _language == Language.vi;

  Future<void> init() async {
    _prefs = await SharedPreferences.getInstance();
    final langStr = _prefs?.getString('language') ?? 'vi';
    _language = langStr == 'en' ? Language.en : Language.vi;
    notifyListeners();
  }

  Future<void> setLanguage(Language lang) async {
    if (_language == lang) return;
    _language = lang;
    await _prefs?.setString('language', lang == Language.en ? 'en' : 'vi');
    notifyListeners();
  }

  String translate(String key) {
    if (_language == Language.vi) {
      return _translationsVi[key] ?? key;
    } else {
      return _translationsEn[key] ?? key;
    }
  }

